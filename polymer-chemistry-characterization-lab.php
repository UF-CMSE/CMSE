            <p>The Polymer Chemistry Characterization Lab (PCCL) is located in Leigh Hall of the Chemistry Department and was 
              dedicated in the summer of 2007.  It is operated by members of the <a href="http://www.chem.ufl.edu/~miller/" target=_blank>Miller</a>, <a href="http://www.chem.ufl.edu/~wagener/" target=_blank>Wagener</a>, and <a href="http://www.chem.ufl.edu/~castellano/" target=_blank>Castellano</a> research groups 
              and the current student director is Matt Burnstein, a third-year graduate student.  True to its name, the PCCL 
              houses a variety of instruments useful for the analysis of polymers and materials.  The lab is approximately 970 square 
              feet and it contains ample counter space and storage for a still unreached capacity of instrumentation and equipment.</p>
            <p>In pursuit of molecular weight data we have a variety of options.  Gel permeation chromatography (GPC) is carried 
              out on a variable-temperature Waters Alliance GPC 2000 in either chloroform or THF against polystyrene standards.  
              Dilute polymer solutions are passed through a column containing a porous gel which creates separation of the solute 
              based on molecular size and gives retention times that are compared to the standards.   While the GPC is invaluable for 
              obtaining data, the solvent restriction can exclude some materials.  Recently two new instruments were purchased to 
              expand the molecular weight determination capabilities of the lab:  a GONOTEC Osmomat 070 vapor pressure osmometer and 
              a GONOTEC Osmomat 090 membrane osmometer.  To obtain molecular weights, the first takes advantage of the vapor pressure 
              of a polymer solution relative to the pure solvent, while the second uses the chemical potential between the two when 
              separated by a semi-permeable membrane.  Though both require a series of dilutions and measurements, there are few solvent 
              restrictions.  Also available is a dynamic light scattering device that provides particle size distribution data.</p>
            <p>Thermal data for polymers and materials is crucial for predicting physical properties, degradation temperatures, phase 
              changes, and more.  The PCCL has three instruments from the TA Instruments Q Series geared towards thermal characterization.  
              Thermal gravimetric analysis (TGA) is carried out on a Q5000, which meticulously measures weight loss over a temperature range.  
              This data is also crucial for safe use of the Q1000 differential scanning calorimeter (DSC), which measures the heat required 
              to maintain a linear temperature change of a sample over time.  This instrument utilizes liquid nitrogen to access a wide 
              temperature range.  A combination of these two analyses is available with the Q600 TGA/DSC combo.  While the DSC capabilities 
              are not that of the stand-alone instrument, the combo provides fast and fairly accurate results that are very useful for project advancement.</p>
            <p>Outside of the above categories, there are other instruments useful for characterizing, visualizing, and even 
              modifying samples.  One of the PCCL’s newest arrivals is a high performance liquid chromatography (HPLC) system, the 
              <a href="http://www.hitachi-hta.com/promotions/hitachi-lachrom-elite-hplc-trade-campaign" target=_blank>LaChrom Elite system from Hitachi</a>.  Sample solutions pass through a column and particles are separated based on their 
              affinity for the stationary phase inside.  Not only are repeatable retention times produced, but the instrument also has 
              UV-Vis and refractive index detectors, thus providing spectroscopic data for the separated fractions as well.  Gas 
              chromatography is also performed in the PCCL on an Agilent 6850 GC.  This is similar to HPLC, however in this case the 
              mobile phase is an inert gas stream and the process is typically faster.  For visualizing materials we can use the Leica 
              DMLP 40x polarizing microscope outfitted with a Leica DFC295 3.0 MP digital camera.  High resolution images are extremely 
              helpful for analyzing and comparing morphologies.  The lab also has a Spectrum One FT-IR for obtaining infrared spectra.</p>
            <p>Rounding up the instrumentation is a P-6000 spin coater from Specialty Coating Systems.  Spin coating is 
              carried out in the PCCL’s particle-free laminar flow hood.  And finally, it would just not feel right to have a 
              laboratory in the chemistry department without any reactions being run; so the PCCL, thankfully, has a hood devoted to hydrogenation.</p>
            <p>—Matt Burnstein</p></td>
