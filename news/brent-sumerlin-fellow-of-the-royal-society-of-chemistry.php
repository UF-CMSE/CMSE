The designation FRSC is given to a group of elected Fellows who have made outstanding contributions to chemistry. The names of newly elected Fellows are published each year in <em>The Times </em>(London). <br><br>
<a href="http://www.chem.ufl.edu/~sumerlin/" target="_blank">Sumerlin’s research</a> at UF focuses on polymer chemistry with particular emphasis on materials that demonstrate stimuli-responsive properties. Potential applications of the technology being investigated are in the area of self-healing materials and controlled delivery in the body and in agriculture. <br>
