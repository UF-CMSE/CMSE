                        <td><img src="../images/CIBAcrew.jpg" alt="Ryan Walczak, John Reynolds, and June Ho Jung have collaborated in the development of new dioxypyrrole polymers in the Ciba Specialty Chemicals program at UF" width="354" height="266"></td>
