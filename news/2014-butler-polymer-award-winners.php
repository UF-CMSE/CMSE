                  <p> <b> William L. Brooks</b><img border="0" src="../images/william_brooks5.jpg" align="right" width="112" height="119"><br>
                    &quot;During my time in the Butler Polymer Research Laboratory, my research has focused on the synthesis and characterization of stimuli-responsive polymer nanostructures. The majority of my work has been focused around the use of boronic acid containing block copolymers. Boronic acids are able to form dynamic covalent bonds with
cis-1,2  and 1,3 diols, like those found in saccharides.  Under physiological conditions, our stimuli responsive, boronic acid-containing  block copolymers form nanostructures, either micelles or vesicles, due to the hydrophobic nature of the incorporated boronic acid units. When the local glucose concentration increases, the glucose binds with the boronic acids, shifting an equilibrium towards the hydrophilic anionic boronate ester, causing the nanostructure to dissociate. By employing these nanostructures as carriers, we are able to deliver therapeutics, like insulin, while protecting those therapeutics, and then release them upon a predetermined trigger. This gives us an approach to mimic the body's own insulin delivery mechanism, whereby a trigger (elevated blood glucose levels) causes a feedback and release of insulin to reduce blood glucose levels. My research is built upon the work of several previous members of the Sumerlin research group, including Drs. Debashish Roy,  Sudershan R.
Gondi, and Jennifer Cambre. Their previous work has laid a great foundation for our continuing interest in boronic acid containing block copolymers. During my time in the Department of Chemistry at UF, I've made some lasting friendships, learned of some amazing new chemistry being done within our halls, and have furthered my own understanding of chemistry and materials. I look forward to my remaining time in the Sumerlin Group and at UF and to finishing my Ph.D. as a Florida Gator.&quot;<br>

