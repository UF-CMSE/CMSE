                    </b>Taylor Gaines is a fourth year graduate student under the advisement of Professor Ken Wagener in the Department of Chemistry here at the University of Florida. A native of the Carolinas, Taylor received his B.S. in chemistry from the University of South Carolina 2011. Professor Brian Benicewicz served as Taylor’s undergraduate research advisor, overseeing research in the area of proton exchange membrane fuel cells. Taylor’s research was composed of polybenzimidazole synthesis, fuel device fabrication, and testing. At the University of Florida he is investigating sophisticated polymer structures containing precisely placed, polar oxidized sulfur functional groups on, and within, polyethylene backbones. His research also includes precision ionomers and microwave assisted ADMET polymerization. He has collaborated with students and groups at Kyoto University, Japan; the University of New South Wales, Australia; and the University of Pennsylvania. Taylor is an active member of the George and Josephine Butler Polymer Laboratory serving as a laboratory coordinator and previously serving as the Polymer Chemistry Characterization Laboratory coordinator. He continues to provide mentorship and training to other graduate students in the Butler Labs. Taylor plans to pursue a career in industry after his anticipated graduation in May, 2015. 
