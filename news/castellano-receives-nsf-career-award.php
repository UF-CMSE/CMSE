                <td width="100%"><h1>Castellano Receives NSF CAREER Award</h1>
                  <p>Professor Ronald K. Castellano has received funding from <a href="http://www.nsf.gov/" target=_blank>NSF</a>'s most prestigious awards program for junior faculty, the <a href="http://www.nsf.gov/funding/pgm_summ.jsp?pims_id=503214" target=_blank>Faculty Early Career Development (CAREER) Program</a>.  CAREER teacher-scholars are selected on the basis of creative, career-development plans that will effectively integrate research and education within the context of the mission of their institution.<br>
                    <br>
                    Castellano plans to use his 5-year career grant to develop new ways to control the behavior of organic molecules in solution and on surfaces through self-assembly.  The research, in the field of supramolecular chemistry, will expand the strategies currently used to tune the materials, optical, and photophysical properties of molecular building blocks for applications that span nanotechnology.  Central to the investigations are through-bond donor-acceptor interactions that have been, until recently, exclusively studied at the molecular level. Castellano and his group will use the tools of organic synthesis, computation, and materials characterization to probe these forces now at the supramolecular level, where they will be used to control the macromolecular properties that emerge from small molecule components. <br>
                    <br>
                    The grant will also fund an integrated outreach program to enhance the chemical and materials science content knowledge of 8-12th grade Florida science teachers.  Castellano, graduate and undergraduate students, and the <a href="http://www.cpet.ufl.edu" target=_blank>Center for Precollegiate Education and Training</a> will join forces to provide much needed inquiry-based activity modules to local teachers through organized workshops and symposia. <br>
                    <br>
                    Castellano joined the <a href="http://www.cpet.ufl.edu/" target=_blank>University of Florida</a> faculty in 2002.  He received his <a href="http://web.mit.edu/chemistry/www/academic/phd.html" target=_blank>Ph.D. in chemistry from MIT</a> in 2000 and was an NSF postdoctoral fellow at the <a href="https://www.ethz.ch/en.html" target=_blank>Swiss Federal Institute of Technology</a> from 2000 to 2002. <br>
