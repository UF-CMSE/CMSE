                  <img src="../images/ken_wagener.jpg" width="140" height="200"  alt="Dr. Ken Wagener" align="right" style="padding-left:5px; padding-bottom:0px" border="1">
<p>

Dr. Ken Wagener has been selected for the 2016 <a href="http://www.polyacs.org/">American Chemical Society</a> Polymer Division <a href="http://www.polyacs.org/254.html">Paul J. Flory Polymer Education Award</a>.  The award, which is presented to one polymer scientist in the nation every other year, recognizes extraordinary contributions to polymer science and engineering. In addition, Wagener is being honored for outstanding achievements in promoting undergraduate and graduate polymer education. The award is named for distinguished American chemist Paul J. Flory, who won the Nobel Prize in Chemistry in 1974 for his research achievements in the physical chemistry of macromolecules.
</p>
<p>Since the award was established, only 19 individuals have been nominated for the Paul J. Flory Polymer Education award. Wagener is the second University of Florida recipient of the award, second to Dr. George B. Butler, who received the nomination in 1990. 
</p>

