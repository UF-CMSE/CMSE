                    <br>
                    This technology is of great interest for the Navy, which spends hundreds of millions of dollars per year to prevent marine growth on its ships.  Algae and barnacle growth increases the drag on ships - causing them to move slower and use more fuel.  Copper-based, toxic paints are currently used to prevent these organisms from adhering, but it also kills other marine life in ports which the ships are docked.  The Sharklet AFTM coating that Brennan has developed is environmentally friendly and does not use toxic chemicals. <br>
                    <br>
                    This research has attracted interest from numerous sources.  In 2007, Brennan was featured on the <a href="http://dsc.discovery.com/tv/shark-week/" target=_blank>Discovery Channel's Shark Week</a> to discuss the Sharklet AFTM coating he developed.  Additionally, the journal Biofouling is currently featuring Brennan's model for adhesion on its cover.  Interest has also been generated in corporate industry.  In 2007, Sharklet Technologies LLC obtained the rights to the Sharklet AFTM patents from the University of Florida and is developing a company based off of this technology. <br>
