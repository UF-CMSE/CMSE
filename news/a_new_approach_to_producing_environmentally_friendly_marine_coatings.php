                <td width="100%"><h2>A New Approach to Producing Environmentally Friendly Marine Coatings<br>
                </h2>
                  <br>
                  <div align="justify">The inspiration for the shark skin model came when Tony Brennan was visiting Pearl Harbor during a conference.  He noticed that submarines coming out of the water were covered in algae and barnacles.  Brennan noted that some marine animals, such as humpback whales, are covered in barnacles.  Yet other animals, such as sharks, have no organisms growing on them.  This led him to believe that there was something about the surface of a shark's skin that prevents adhesion of algae and barnacles.  When Brennan returned to Florida, he and a colleague caught a live shark off the Atlantic coast and made an imprint of its skin with dental material.  After analyzing the imprint, Brennan and his research group realized that the surface of the shark's skin was similar to models of an ideal hydrophobic surface which they were researching. <br>
                    <br>
                    Brennan's group recreated the shark skin out of silicone using processing techniques typically used in the microelectronics industry.  Brennan showed that if the features of the shark scales are slightly smaller than the spore of an algae plant, then the Sharklet AFTM surface could inhibit adhesion by approximately 75%.  Brennan has shown a similar response for other organisms, such as barnacles and bacteria.
