                      <p>&quot;The investigation of biochemical processes is closely knit to probing both structural and dynamic features of biomolecules. Electron Paramagnetic Resonance (EPR) Spectroscopy, in conjunction with site-directed spin labeling (SDSL), is a useful biophysical technique being utilized by Dr. Fanucci’s group to study the dynamics of structurally diverse biomolecules. These include membrane proteins in lipid environments, natively unstructured proteins, HIV-1 protease, and RNA riboswitches. Through incorporation of a paramagnetic nitroxide spin label, EPR experiments can elucidate information regarding backbone dynamics, conformational changes, local secondary structure, and even distance information between nitroxide probes. Important information gained from EPR studies, for example for the HIV- protease project, has elucidated shifts in occupancy of conformational sampling of the HIV-1 protease, which is important for drug-resistant related considerations. Through use of EPR, biologically significant questions about structure and dynamics can be answered to further understanding of biochemical processes and biomolecular function.&quot;</p>
