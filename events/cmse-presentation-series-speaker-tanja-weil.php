                  <p><strong><font size="+1"><a name="mse">About Tanja</a></font></strong>                  
<p>Dr. Professor Tanja Weil, Max Planck Institute for Polymer Research, Germany</p>
<p>This presentation will address the impact of metathesis-based polymerization techniques such as ring-opening metathesis polymerization (ROMP), 1-alkyne polymerization and the cyclopolymerization of diynes on polymer-based material science. Along with aspects of polymerization and photo-catalysis, the design and tailor-made synthesis of functional polymeric materials will be addressed. Examples include stationary phases for selective metal extraction, analytical- and preparative-scale chromatography and large-volume applications such as the downstream processing of fermentation broths. Furthermore, functional supports for molecular, continuous heterogeneous catalysis (Figure 1) and continuous electro(bio-)catalysis will be presented. Finally, the synthesis of nanoparticle-reinforced polymeric, porous scaffolds from biodegradable monomers and their use in regenerative medicine will be demonstrated. </p><br><br>

