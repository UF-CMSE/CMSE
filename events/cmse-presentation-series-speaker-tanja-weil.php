<p><strong><font size="+1"><a name="chem">CO<sub>2</sub>- and Metal-Protected N-Heterocyclic Carbenes as Truly Latent Catalysts in Polymerization (Organo-) Catalysis</a></font></strong></p>
<p>Professor Dr. Tanja Weil, Max Planck Institute for Polymer Research, Germany</p>
<p>The research of Tanja Weil centers around tailored nanomaterials for biomedical and materials science applications. She designs and synthesizes precision macromolecules and hybrid materials that can, for example, enhance the uptake of viruses into stem cells thus offering great opportunities to improve gene therapy. Moreover, such tailored macromolecules can detect cancer cells and induce light-controlled cell deaths.</p>
