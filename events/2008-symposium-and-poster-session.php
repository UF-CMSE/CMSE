              The Center for Macromolecular Science and Engineering held its 2008 Symposium and Poster Session on Thursday, January 17. The MacroCenter invited three speakers to share their research- two from universities and one from industry- to enhance the goal of promoting interdisciplinary interactions among our students and faculty. A poster session and reception followed the seminars in the newly renovated Kathryn Chicone Ustler Hall Atrium on campus. Forty-four graduate students and postdocs presented, with contributions from 16 different research groups comprising the CMSE (the MacroCenter). <br />
              <br />
              Dr. Harry Gibson, a Professor from Virginia Technical University, is spending his spring here at UF while on sabbatical.  He was the first to speak, and his seminar was titled: <i>"Supramolecular Self-Assembly of Polymeric Systems"</i>, which focused on his group's well-known polyrotaxane chemistry as employed in alternate supramolecular assembly schemes. <br />
              <br />
              Dr. Eric Moskala of Eastman Chemical Company presented the second seminar.  Eastman Chemical Company has been a long time supporter of the polymer research effort here.  His talk was titled: <i>"New Developments in Specialty Copolyesters at Eastman Chemical Company."</i> Dr. Moskala brought us up to date on Eastman's well-known amorphous copolyester product line, which has been widely accepted in the marketplace for a long time now. <br />
