                    The final seminar was presented by Professor Jeff Moore from the University of Illinois, titled <i>"Stress-Activated Reactions in Polymeric Materials"</i>, a report that provided a nice view of the latest approach to mechano-chemical chemistry as found in self-healing and signal generation responsive materials. <br />
                    <br />
                    Following the symposium, participants walked over to the poster session held in Ustler Hall, where the posters were judged for their effectiveness. The judges told the directors of the MacroCenter that this was one of the highest quality poster sessions that they had attended. Three posters were eventually selected for special recognition and a $300 prize. <br />
