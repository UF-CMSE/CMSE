                    <img src="../images/schuter2.jpg" width="200" height="258" align="right"><a name="dieter">Dieter Schlüter</a> has been full professor of polymer chemistry at the <a href="https://www.mat.ethz.ch/" target="_blank">ETH Zürich Department of Materials</a> since spring 2004. Born in Germany, he studied chemistry and geophysics at the <a href="http://www.en.uni-muenchen.de/index.html" target="_blank">University of Munich</a> (LMU) and received a doctorate in organic chemistry 1984 with Prof. G. Szeimies. After post-doctoral work with Prof. K. <a href="http://chem.berkeley.edu/faculty/vollhardt/index.php" target="_blank">P. C. Vollhardt (UC Berkeley)</a> and <a href="http://www.dur.ac.uk/chemistry/staff/profile/?id=1291" target="_blank">Prof. W. J. Feast (U of Durham, England)</a> he joined the <a href="http://www.mpg.de/en" target="_blank">Max Planck Institute</a> for Polymer Research in Mainz in 1986, where he concentrated on synthetic macromolecular chemistry in the department of <a href="http://www.chem.ufl.edu/~wagener/" target="_blank">Prof. G. Wegener</a>. In 1991 he received habilitation in organic chemistry at the <a href="http://www.uni-mainz.de/eng/" target="_blank">University of Mainz</a>. Shortly thereafter he was briefly an associate professor at the <a href="http://www.kit.edu/english/">Polymer Institute of the University of Karlsruhe</a> before accepting a chair professorship for organic and macromolecular chemistry at the <a href="http://www.fu-berlin.de/en/" target="_blank">Free University of Berlin</a> in 1992, where he was until spring 2004. He has held and still holds numerous functions in organizations, on boards, in committees and alike. Presently he has about 250 peer-reviewed publications and several book chapters, and gave approximately 250 invited lectures all over the world. His research achievements are: 
                    (1) Discovery of the Suzuki polycondensation, a powerful and worldwide used method for the synthesis of polyarylenes which made it into industrial application, (2) exploration of the polymerization of [1.1.1]propellanes, where CC sigma bonds can be made polymerize, (3) discovery of the first successful Diels-Alder polymerizations, through which access to high molar mass, soluble ladder-type polymers was provided,  (4) recognized contributions to the field of shape-persistent macrocycles and fully aromatic double-stranded macrocycles, (5) discovery of the "dendronized polymers" as the largest synthetic and structurally defined macromolecules, (6) discovery of the first rational synthetic routes to two-dimensional (sheet-like) polymers.<br>                  
