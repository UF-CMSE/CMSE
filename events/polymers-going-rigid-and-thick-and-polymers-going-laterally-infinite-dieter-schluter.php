<p><strong><font size="+1"><a name="rigid">Polymers Going Rigid and Thick</a></font></strong></p>
<p>By A.  Dieter Schl&uuml;ter, ETH Zurich</p>
<p>After a brief excursion into the discovery  of Suzuki polycondensation in 1988, some of the very recent exciting findings  using this powerful method for the synthesis of polyarylenes will be described.  One of them shows the Nobel-prize winning Suzuki cross-coupling reaction to be arguably  the best method for CC bond formation.<a name="OLE_LINK1"><sup>[1]</sup> </a>In  a second part it will be discussed how conventional polymer chains can be  systematically thickened to the degree that they attain a persistent  cylindrical shape and turn into molecular objects. A particular representative,  a fifth generation dendronized polymer, is the largest ever synthesized  macromolecule with structure precision. Quite some emphasis will be placed upon  the aspect why thickening of polymer chains makes sense and to which  applications this can lead.<sup>[2]</sup></p>
