  [2] B.  Zhang, R. Wepf, K. Fischer, M. Schmidt, S. Besse, P. Lindner, B. T. King, R.  Sigel, P. Schurtenberger, Y. Talmon, Y. Ding, M. Kr&ouml;ger, A. Halperin, A. D.  Schl&uuml;ter, <em>Angew. Chem. Int. Ed</em>. <strong>2011</strong>, <em>50</em>, 737-740.</p>
