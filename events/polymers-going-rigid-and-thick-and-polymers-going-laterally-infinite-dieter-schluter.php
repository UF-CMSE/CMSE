<p><strong><font size="+1"><a name="infinite">Polymers Going Laterally Infinite</a></font></strong></p>
<p>By A.  Dieter Schl&uuml;ter, ETH Zurich</p>
<p>The present interest in graphene, a  naturally occurring two-dimensional polymer, makes clear that there is no  synthetic method available that would allow accessing a covalently bonded molecular  sheet with internal periodicity and a thickness of one monomer unit only.<sup>[1]</sup>  After a brief overview of &ldquo;organic&rdquo; and &ldquo;polymer&rdquo; approaches performed so  far, the concepts will be presented which are presently being pursued in the  author&rsquo;s laboratory. They rest upon carefully designed monomers, interfacial as  well as single crystalline ordering, and both metal-complexation and  light-induced polymerizations. The lecture will provide a state-of-the-art  picture including the not yet published first solution to the problem. </p>
