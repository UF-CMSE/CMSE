            <p align="left"> The Center for Macromolecular Science and Engineering held its 2010 Symposium and Poster Session on Thursday, April 1. The MacroCenter invited Prof. Dr. Katharina Landfester to speak and enhance the goal of promoting interdisciplinary interactions among our students and faculty. A poster session and reception followed the seminars in the newly renovated Kathryn Chicone Ustler Hall Atrium on campus. Thirty-six graduate students and postdocs presented, with contributions from 16 different research groups comprising the CMSE (the MacroCenter). <br />
              <br />
              Prof. Dr. Katharina Landfester, Director of the <a href="http://www.mpip-mainz.mpg.de/physical_chem_of_polymers" target=_blank>Max Planck Institute for Polymer Research</a> (Germany) held two seminars titled <i>"Functional nanostructured particles obtained by the miniemulsion process"</i> and <i>"From particles to complex nanocapsules for materials science".</i> <br />
              <br />
              Following the symposium, participants walked over to the poster session held in Ustler Hall, where the posters were judged for their effectiveness. The judges told the directors of the MacroCenter that this was one of the highest quality poster sessions that they had attended. Three posters were eventually selected for special recognition and a $300 prize. <br />
