                  <p><font size="2">Functional nanostructures are in the focus of current soft matter science. They occur in advanced synthetic as well as in biological systems through self-assembly of carefully chosen building blocks. Secondary interactions such as hydrogen bonding, aromatic pi-interactions, and electrostatic forces are of central importance. Here, magnetic resonance provides unique and highly selective information on structure and dynamics of such systems<sup>1</sup>, e.g., on hydrogen bond networks in the solid state,<sup>2</sup> stacking, and cooperative molecular motions of discotics<sup>3</sup> and macrocycles<sup>4</sup>. Solid state NMR is also able to elucidate self-assembly, conformation and dynamics of polypeptides<sup>5</sup>. <br><br>
The phase separation and dynamic heterogeneities of thermoresponsive dendritic polymers can conveniently be studied with a desktop EPR spectrometer<sup>6</sup>. With more advanced pulsed EPR techniques, such as DEER, distances on the scale of several nm can be measured. This has recently been used to unravel the structure of Human Serum Albumin, a versatile transport protein in the blood. It was found that the functional protein structure contains a more rigid, asymmetric inner part, while the surface of the protein shows much larger structural flexibility<sup>7</sup>. <br><br>
For full structural and dynamic elucidation, the spectroscopic data have to be combined with other techniques, in particular X-ray scattering, microscopy, dielectric spectroscopy and last, but not least, quantum chemical calculations. The findings will be related to the function of such materials, such as proton- and photo-conductivity.
