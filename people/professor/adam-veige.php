                  <p align="justify">Adam Veige received a Hons. B.Sc. degree in Chemistry in 1997 from the University of Western Ontario, Canada. For a period of six months, Dr. Veige worked on the synthesis of chiral titanium catalysts for stereoselective polyolefin synthesis in the laboratories of Dr. David McConville at the University of British Columbia. Dr. Veige obtained his Ph.D. in 2003 at Cornell University while working with Dr. Peter T. Wolczanski. His Ph.D. research at Cornell focused on kinetic and mechanistic investigations of O-atom transfer and a rare metal-olefin to metal-alkylidene isomerization. His postdoctoral work under the guidance of Dr. Daniel G. Nocera, at the Massachusetts Institute of Technology, focused on elucidating the mechanism of photocatalytic H2 evolution from rhodium and iridium mixed-valent complexes. In 2004 he joined the Department of Chemistry at the University of Florida as Assistant Professor in Inorganic Chemistry. Dr. Veige’s research focuses on the synthesis of highly active catalysts for creating value-added products including the polymerization of olefins and alkynes. Another area of current interest is the synthesis of in-chain metallopolymers using iClick technology invented by the Veige group. In 2010, Dr. Veige was named Director for the Center for Catalysis, and in 2011 was promoted to Associate Professor with tenure. Dr. Veige was awarded a Camille and Henry Dreyfus New Faculty Award, an Alfred P. Sloan Fellowship, an NSF Career award, the Dr. Paul Tarrant Fellowship, and the University of Florida Undergraduate Mentor of the Year Award (2012).  Dr. Veige’s research is currently funded by the NSF, DOE-BES, ACS-PRF, and the University of Florida, Office of Technology Licensing.</p><br>
