                  <p align="justify"> The Polymer Program at the Department of Chemistry, the <a href="http://butlerlabs.chem.ufl.edu/">George and Josephine Butler Polymer Research Laboratory</a>, and the Center for Macromolecular Science and Engineering are pleased to announce that Dr. Brent Sumerlin will be joining our research and teaching efforts here at the University of Florida in the summer of 2012.  Currently the Harold Jeskey Associate Professor in Chemistry at Southern Methodist University in Dallas, Texas, Dr. Sumerlin comes to us with a wide range of expertise in the areas of materials chemistry and the life sciences. 
                  <p align="justify">He graduated from North Carolina State University in 1998 with a B.S. in Textile Chemistry, and received his Ph.D. in 2003 at the University of Southern Mississippi under the direction of Dr. Charles McCormick.  He continued his work as a Visiting Assistant Professor/Postdoctoral Research Associate in the group of Krzysztof Matyjaszewski at Carnegie Mellon University from 2003-2005.  In 2005 he joined the Department of Chemistry at Southern Methodist University as an Assistant Professor.  In the spring of 2007, he was named the Harold Jeskey Trustee Assistant Professor in Chemistry, and in 2009 was promoted to Associate Professor with tenure.  His research involves the synthesis and investigation of stimuli-responsive polymeric materials with potential applications as controlled release/delivery systems.
                  <p align="justify">His work has been well recognized in the scientific community.  In 2011 he was named a Kavli Fellow (Frontiers of Science, National Academies of Sciences).  In 2010 he was named an Alfred P. Sloan Research Fellow and a Gerald J. Ford Research Fellow. In 2008 he received the NSF CAREER award and has also received the ACS Leadership Development Award.  Dr. Sumerlin's research has been published in 64 peer-reviewed articles, and in 2007 he had the #1 most referenced article in <em>Macromolecules</em>.<br><br><center><img src="../../images/brent_sumerlin_2.jpg" alt="Brent Sumerlin Group Photo"><br><br><img src="../../images/brent_sumerlin_4.jpg" alt="Brent Sumerlin Group Members"></center>
