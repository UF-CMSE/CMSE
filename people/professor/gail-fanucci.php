                  <p align="justify">The <a href="http://butlerlabs.chem.ufl.edu/">George and Josephine Butler Polymer Research Laboratory</a>, and the Center for Macromolecular Science and Engineering are pleased to announce that Dr. Gail Fanucci will be joining our research and teaching efforts.   
                  <p align="justify">Gail Fanucci received a B.S. degree in Biochemistry and a B.S. degree in Biophysics in 1994 from the University of Scranton. Under the advisement of Dr. Daniel R. Talham, she earned her Ph.D. in Chemistry, with a concentration in Physical Chemistry, from the University of Florida in 1999. Her postdoctoral work, at the University of Pennsylvania, focused on utilization of solid state NMR for the study of membrane proteins in the lab of Dr. Stanley J. Opella. She continued postdoctoral work with Dr. David S. Cafiso, at the University of Virginia, studying membrane proteins through use of site-directed spin labeling and EPR spectroscopy. In 2004 she joined the Department of Chemistry at the University of Florida as Assistant Professor in both the Physical Chemistry and Biochemistry divisions. She received an NSF Career award in 2008 and was promoted to Associate Professor with tenure in 2011. Her research is currently funded by NSF and NIH awards.
