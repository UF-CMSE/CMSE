                  <p><img src="/images/coray-colina.jpg" alt="Dr. Coray Colina" style="float:right; PADDING-LEFT: 5px; padding-bottom: 5px">The Polymer Program at the Department of Chemistry, the&nbsp;<a href="http://butlerlabs.chem.ufl.edu/"><strong>George  and Josephine Butler Polymer Research Laboratory</strong></a>, and the  Center for Macromolecular Science and Engineering are pleased to announce that  Dr. Coray Colina will be joining our research and teaching efforts here at the  University of Florida in August 2015. Currently an Associate Professor of Materials Science and Engineering at <a href="http://www.psu.edu/" target="_blank">Pennsylvania State University</a>, Dr. Colina comes to us with  a range of expertise in the areas of biomaterials, computational materials and polymers.<br>
                    <br>
                    Professor Colina obtained her Ph.D. at the <a href="http://www.ncsu.edu/" target="_blank">North Carolina State University</a> (2004) and her B.S. (1993) and M.Sc. (1994) at <a href="http://www.usb.ve/" target="_blank">Simón Bolívar University</a>. She was a Postdoctoral Research Associate in the Department of Chemistry at the <a href="http://www.unc.edu/" target="_blank">University of North Carolina at Chapel Hill</a>. She has been a faculty member at Simón Bolívar University and joined the Department of Materials Science and Engineering at The Pennsylvania State University as Associate Professor in January 2007. She won the 1999 Award for Outstanding Teaching Achievement (at the Assistant Professor level) at Simón Bolívar University, as well as several other awards from the Venezuelan's National Committees from the Development of Higher Education and for the Academic Advancement. She has several international collaborations and has presented the results of her research globally in more than 175 national and international conferences. She has published over 80 papers (including conference proceedings).<br>
                    <br>
                  Her research group strives to understand and predict  structure-property relations in functional materials, such as polymeric  membranes, biomolecules, and alternative ionic liquids. They use a variety of  simulation techniques to gain further understanding of these systems by  providing unique insight into structural aspects and phenomena. Complementary  to experimental investigations, their work is helping to analyze and interpret  experimental results, as well as to predict performance of new materials to  guide future experimental design efforts.<br>
                  
                  She has received funding from the NSF, NIH, ACS-PRF, MedImmune, Saint Gobain, and Dow, among others.  While at Penn State she led the <a href="http://www.matse.psu.edu/undergraduate/research-opportunities/reu" target="_blank">REU in Soft Materials</a> program for 7 years. She is a member of several professional organizations and currently serve as the (Elected) Chair for the <a href="http://comsef.org/" target="_blank">Computational Molecular Science and Engineering Forum of the AIChE</a>. She is looking forward to joining UF! </p>
