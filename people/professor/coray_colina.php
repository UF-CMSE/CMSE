                  <p>The Polymer Program at the Department of Chemistry, the&nbsp;<a href="http://butlerlabs.chem.ufl.edu/"><strong>George  and Josephine Butler Polymer Research Laboratory</strong></a>, and the  Center for Macromolecular Science and Engineering are pleased to announce that  Dr. Daniel Savin will be joining our research and teaching efforts here at the  University of Florida in January 2015. Currently an Associate Professor of  Polymer Science and Engineering in the School of Polymers and High Performance  Materials at the <a href="http://www.usm.edu/" target="_blank">University of Southern Mississippi</a>, Dr. Savin comes to us with  a range of expertise in the areas of materials chemistry and polymer  characterization.<br>
                    <br>
                    He graduated from <a href="https://www.hmc.edu/" target="_blank">Harvey Mudd College</a> (Claremont, CA) in 1995 with  a B.S. in Chemistry and received an M.S. in Polymer Science and a Ph.D. in  Chemistry in 1997 and 2002, respectively, at <a href="http://www.cmu.edu/index.shtml" target="_blank">Carnegie Mellon University</a> under  the direction of Prof. Gary Patterson.&nbsp;  His early research involved the study of dynamics in glassy  materials.&nbsp; He continued this work as a  Postdoctoral Fellow in the group of Prof. Timothy Lodge at the University of  Minnesota from 2002-2003.&nbsp; In 2003 he  joined the <a href="http://www.uvm.edu/~chem/" target="_blank">Department of Chemistry at the University of Vermont</a> as an Assistant  Professor and in 2008 he moved to the <a href="http://www.usm.edu/polymer" target="_blank">School of Polymers at Southern Miss</a>.&nbsp; His current research involves the synthesis  and characterization of stimuli-responsive peptide block copolymers for use in  controlled release applications.&nbsp; He has  additional projects studying impact energy-absorbing materials for use in  personal protective equipment, and in developing next-generation oil  dispersants.<br>
                    <br>
                    His work has been well-recognized in the scientific  community.&nbsp; He has published 32  peer-reviewed articles and has received funding from the NSF, ONR, DOE, Gulf of  Mexico Research Initiative, and the Camille and Henry Dreyfus Foundation.&nbsp; While at Southern Miss he led the summer REU  program and was highly active in community outreach at the K-5 level.&nbsp; He is a member of the ACS PMSE Executive  Committee, the POLYED Executive Committee, and serves as secretary of the  Intersociety Polymer Education Council.&nbsp; He  is looking forward to continuing this work at UF!<br>
