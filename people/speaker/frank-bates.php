                <h1>
                  <center>
                    Spring 2018 Seminar Schedule
                  </center>
                </h1>
                <p> <b>Seminar 1 - Monday, 2/5/18 11:30am, LEI 309</b><br>
                  <i>Multiblock Polymers: Panacea or Pandora’s Box?</i><br>
                  Advances in synthetic chemistry present scientists and engineers with an
                  inexhaustible palette of macromolecular design possibilities. This lecture reviews
                  opportunities and challenges associated with block polymers </p>
                <p> <b>Seminar 2 - Tuesday, 2/6/18 4:00pm, JHH 221</b><br>
                  <i>Polymer Chain Configuration</i><br>
                  Stringing together numerous repeat units leads to a seemingly daunting array of
                  polymeric configurational states. This lecture outlines the statistical concepts that
                  lead to a simple Gaussian coil description that underpins polymer melt and solution
                  structure and physical properties. </p>
                <p> <b>Seminar 3 - Friday, 2/9/18 12:00pm, LEI 309</b><br>
                  <i>Polyolefins: Big, Bad and Beautiful</i><br>
                  Saturated hydrocarbon polymers dominate the landscape of commercial plastics.
                  Yet there is no conclusive explanation of why polyethylene and polypropylene
                  phase separate in the melt state. This lecture will review the classic theory of
                  polymer-polymer thermodynamics and describe recent advances in polyolefin
                  blends. </p>
                <p> <b>Seminar 4 - Monday, 2/12/18 4:00pm, LEI 309</b><br>
                  <i>Block Copolymer Thermodynamics: A Toy Model</i><br>
                  Although the detailed theory of block copolymer phase behavior is mathematically
                  complex, the basic ingredients are easy to comprehend. This lecture will provide a
                  surprisingly simple approach to predicting order and disorder in diblock
                  copolymers based on concepts developed in lectures 1-3. </p>
                <p> <b>Seminar 5 - Monday, 3/26/18 12:00pm, LEI 309</b><br>
                  <i>Fluctuation Effects in Block Copolymers-Homopolymer Blends </i><br>
                  Block copolymers can function as macromolecular amphiphiles resulting in phase
                  behavior analogous to that associated with mixtures of low molecular weight
                  surfactants when mixed with oil and water. This lecture will describe recent
                  advances in understanding ternary mixtures of A-B diblock copolymers with the
                  corresponding A and B homopolymers. </p>
                <br>
                <br>
                <p> <b>Seminar 6 - Wednesday, 3/28/18 4:00pm, LEI 309</b><br>
                  <i>Scattering of Light, X-rays and Neutrons</i><br>
                  This lecture will develop the fundamental principles behind the scattering of
                  radiation from condensed matter and describe the unique and complimentary
                  applications of visible light, x-rays and neutrons to the study of polymer structure. </p>
                <p> <b>Seminar 7 - Friday, 3/30/18 12:00pm, LEI 309</b><br>
                  <i>Chain Exchange Dynamics in Block Copolymer Micelles</i><br>
                  Block copolymers form nanoscale micelles when dispersed in selective solvents at
                  low concentrations. Equilibrating such solutions requires the exchange of polymers
                  between micelles. Time-resolved small-angle neutron scattering experiments have
                  revealed a remarkable sensitivity to molecular weight and molecular architecture,
                  which will be discussed in this lecture. </p>
                <p> <b>Seminar 8 - Monday, 4/2/18 4:00pm, LEI 309</b><br>
                  <i>Toughening Thermoset Plastics with Block Copolymer Micelles</i><br>
                  Thermoset epoxies find numerous applications owing to benefits associated with
                  easily processed liquid resins that cure into hard and durable plastics. However, this
                  class of materials suffers from brittle mechanical behavior. Dispersion of block
                  copolymer micelles can lead to remarkable improvements in toughness, which will
                  be discussed in this lecture. </p>
                <br>
                <br>
                <p> <b>Seminar 9 - Wednesday, 4/4/18 4:00pm, LEI 309</b><br>
                  <i>Sustainable Plastics based on Graft-block Molecular Architectures</i><br>
                  Synthetic polymers offer society innumerable benefits that raise our standard of
                  living. However, most plastics end up in a landfill or liter the environment. The
                  development of commercially viable polymers that combine superior performance
                  and competitive cost with sustainability represents a major challenge. This lecture
                  will review recent advances in the application of graft-block molecular
                  architectures, which offer promising solutions to these challenges. </p>
                <p> <b>Seminar 10 - Friday, 4/6/18 2:00pm, JHH 221</b><br>
                  <i>This series of lectures closes with a presentation that highlights recent discoveries
                  regarding the formation of complex low symmetry Frank-Kasper phases and
                  quasicrystals in simple compositionally asymmetric diblock copolymer melts. I will
                  make the case that the secrets of nature exposed during spontaneous symmetry
                  breaking are most transparently revealed and understood through experiments and
                  theory dealing with block copolymers. </p>
