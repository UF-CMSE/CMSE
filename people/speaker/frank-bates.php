                <p> <img src="../../images/frank-bates.jpg" alt="Dr. Robert Waymouth" width="200" style='float: right; margin: 0px 0px 20px 20px;'> The Polymer Program at the Department of Chemistry, the&nbsp;<a href="http://butlerlabs.chem.ufl.edu/"><strong>George and Josephine Butler Polymer Research Laboratory</strong></a>, and the  Center for Macromolecular Science and Engineering are pleased to announce that  Dr. Frank Bates will be presenting the 2018 <a href="http://butlerlabs.chem.ufl.edu/butler-lectureship-series.php">Butler Lectureship Series</a>.<br>
                  <br>
                  Frank Bates is currently a Regents Professor at the <a href="https://www.cems.umn.edu/">University of Minnesota</a>. Dr. Bates conducts research on a range of topics related to polymers, with a particular focus on the thermodynamics and dynamics of block copolymers and blends. He won the 2004 David Turnbull Lectureship Award from the Materials Research Society, shared the ACS Cooperative Research Award in 2008, and was awarded the 2008 Sustained Research Prize by the Neutron Scattering Society of America. In 1989, Dr. Bates received the Dillon Medal and in 1997 the Polymer Physics Prize, both from the American Physical Society where he is a Fellow. In 2002, Dr. Bates was elected to the US National Academy of Engineering and in 2005, he was elected a Fellow of the American Association for the Advancement of Science. Dr. Bates received a BS in Mathematics from SUNY Albany in 1976 and an M.S. and a Sc.D. in Chemical Engineering from MIT in 1979 and 1982, respectively.</p>
                
