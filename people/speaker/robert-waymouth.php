                  <h1><center>Spring 2016 Seminar Schedule</center></h1>
                  
                  <p>
                    <b>Seminar 6 - Monday, 2/15/16 12:00pm, LEI 309</b><br>
                    <i>Organocatalytic Synthesis of Biodegradable and Biocompatible Polymers</i><br>
                    The high functional group tolerance of organic catalysts allows for the generation
                    of highly functionalized biocompatible and biodegradable oligomers.  This lecture
                    will describe the synthesis and properties of functional polymers and some of their
                    biological applications.
                  </p>
