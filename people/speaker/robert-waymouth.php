                    <b>Seminar 7 - Wednesday, 2/17/16 12:00pm, LEI 309</b><br>
                    <i>Catalytic Polymerization: The Legacy of Ziegler, Natta, Hogan, and Banks</i><br>
                    Pioneering advances in olefin polymerization catalysis in the 1950's spawned a vast
                    industry.  This lecture will describe the relationship between the science, technology,
                    and properties of olefin polymers and the challenges and opportunities for new catalyst
                    developments.  
                <p>
                    <b>Seminar 8 - Thursday, 2/18/16 4:00pm, LEI 207</b><br>
                    <i>Dynamic Catalysis:  A Strategy for Control of Polymer Sequence</i><br>
