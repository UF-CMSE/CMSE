                  The Polymer Program at the Department of Chemistry, the&nbsp;<a href="http://butlerlabs.chem.ufl.edu/"><strong>George and Josephine Butler Polymer Research Laboratory</strong></a>, and the  Center for Macromolecular Science and Engineering are pleased to announce that  Dr. Robert Waymouth will be presenting the 2015 <a href="http://butlerlabs.chem.ufl.edu/butler-lectureship-series.php">Butler Lectureship Series</a>.<br>
                    <br>

                    Robert Waymouth is the Robert Eckles Swain Professor of Chemistry at <a href="http://www.stanford.edu/">Stanford University</a>. He received B.S. in Mathematics and B.A. in Chemistry from Washington and Lee University and his Ph.D. in Chemistry at the Caltech in 1987 with Professor R.H. Grubbs. He was a postdoctoral fellow with the late Professor Piero Pino at the ETH in Zurich in 1987 and joined the faculty at Stanford as an Assistant Professor in 1988. He received the Alan T. Waterman Award from the NSF in 1996, the Cooperative Research Award in Polymer Science in 2009, and EPA's Presidential Green Chemistry Challenge Award in 2012 with Dr. James Hedrick. He has won several university teaching awards, including the Walter J. Gores Award, the Phi Beta Kappa Teaching Award, and is currently a Bass Fellow in Undergraduate Education. His research interests are at the interface of Inorganic, Organic and Polymer Chemistry, in particular the development of new concepts in catalysis for the selective synthesis of both macromolecules and fine chemicals. Particular areas of interest include catalytic polymerization reactions, the development of organocatalytic polymerization strategies, and selective oxidation catalysis.</p>
                   
                   <p> 
                    Dr. Waymouth's <i>"Catalysis: The Enabling Science for Polymer Chemistry"</i> will be presented throughout the Fall 2015 and Spring 2016 semester. Seminar times and location are listed below.
