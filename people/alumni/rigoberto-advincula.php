                    <p><img src="../../images/rigoberto-advincula-1.jpg" alt="Rigoberto Advincula" width="224" height="314" align="right">Rigoberto Advincula, or “Gobet”, is Professor at <a href="http://www.case.edu/" target="_blank">Case Western Reserve University</a>,<a href="http://engineering.case.edu/emac/" target="_blank"> Department of Macromolecular Science and Engineering</a>. He obtained his Ph.D. in Chemistry in 1994 working with Randy Duran (now at LSU) and his B.S. in <a href="http://www.chem.upd.edu.ph/" target="_blank">Chemistry at the University of the Philippines</a> in 1987. He did two post-docs: with Wolfgang Knoll (now at AIT) at the <a href="http://www.mpip-mainz.mpg.de/2907/en" target="_blank">Max Planck Institute for Polymer Research</a> and with Curt Frank at Stanford University.<br><br>
                      Gobet is a Fellow of the American Chemical Society, Fellow of the Polymer Chemistry Division, and Fellow of the PMSE Division. He obtained his NSF CAREER in 1999 and has been awarded the <a href="http://www.paase.org/kohawards.html" target="_blank">Koh Lecture Award (PAASE)</a>, <a href="http://pmse.sites.acs.org/dolittleaward.htm" target="_blank">Arthur Doolittle Award (PMSE Division)</a>, Science Excellence Honoree (PhilDev), FSCT Technical Lecturer (ACA), and<a href="http://www.humboldt-foundation.de/web/home.html" target="_blank"> Alexandar von Humboldt Research Fellow</a>. He has been Visiting Professor in a number of Universities abroad. Currently he serves as Editor of Reactive and Functional Polymers (Elsevier), Macromolecular Research (Springer) and Editorial Advisory Board on Chemistry of Materials (2007-2012), Macromolecules (2007-2009), ACS Applied  Materials and Interfaces, Journal of Applied Polymer Science, and Journal of Biocompatible Polymers. He has recently been elected as Vice-Chair of the ACS Polymer Chemistry Division. He has been active with the Division for many years serving as Alternate Councilor, Workshop Committee co-chair, Treasurer, and Organizer of a number of Symposia at ACS national meetings and Pacifichem on topics including electro-optical polymers, nanostructured materials, polymer interfaces, and biosensing. He has co-organized Macromex 2008, 2011, Polymer Biennial 2007, and the US-Japan Workshop on Energy Related Polymers in 2007.  Other activities include involvement with the Philippine-American Academy of Science and Engineering (PAASE) where he served as President in 2012 and the PhilDev Foundation.<br><br>
                      Gobet’s research interests include: Design, synthesis, and characterization of novel polymers and nanostructured materials capable of controlled-assembly, tethering, and self-organization in ultrathin films. This includes functional macromolecules, coordination polymerization, polymerization on surfaces, electropolymerization, and preparation of nanoparticles and hybrid materials. Properties include: smart coatings, electrically conducting, photoluminescent, electroluminescent, energy harvesting, optically active, and biocompatible. Surface sensitive spectroscopy and microscopy is systematically utilized to probe materials properties and biological phenomena. His group is also involved in investigating nanoparticles, nanostructured surfaces, and nanocomposite materials for sensor and bioapplications. Other applied studies include packaging, coatings, biomaterials, plastics, and process development. Through these research efforts, he has mentored a number of Ph.D. students, MS Students, undergraduates, high school and visiting scholars. He has published close to 500 papers (H-index=41) and presented close to 600 talks. <br><br>
                      More information about Prof. Advincula and his group can be found at:<br>
