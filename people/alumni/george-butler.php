                <td width="100%" height="577" valign="top"><p align="justify">George B. Butler, 91, the polymer chemist who introduced macromolecules to the University of Florida, died June 7th, 2007, in Gainesville, Florida. Born and raised in Liberty, Mississippi, educated at <a href="http://www.smcc.cc.ms.us/" target=_blank>Southwest Mississippi Junior College</a>, <a href="http://www.mc.edu/" target=_blank>Mississippi College</a>, and the <a href="http://www.unc.edu" target=_blank>University of North Carolina/Chapel Hill</a>, Professor Butler was known as a kind and considerate gentleman who offered quiet advice and genuine friendship every day of his life. Professor Butler's introduction to polymer chemistry came as a researcher at the <a href="http://www.rohmhaas.com" target=_blank>Rohm & Haas Company</a> in Philadelphia in the early 1940s, where he pioneered the synthesis of polymeric binders for propellants. In 1946 he and his wife Josephine moved to Gainesville, where he began teaching as an instructor at the University of Florida. Today, 61 years later, more than 18 faculty and 120 students study macromolecular science and engineering on campus, a tribute to his remarkable academic career. <br>
                  <br>
                  The Butler research program is perhaps best known for its discovery of cyclopolymerization, chemistry that has proven to be of enormous academic interest and practical importance. The $10,000 research grant he received from the <a href="http://www.navy.mil/">Navy</a> to pursue this chemistry was one of the first external grants to be awarded on campus. Well over 4,000 publications have dealt with various aspects derived from the original discovery.  His and Professor Paul Tarrant's entrepreneurial spirit led to the establishment of Peninsular Chem Research Corporation in Gainesville, thereby spawning commercial development worldwide in the water treatment, textiles, cosmetics, paper, coal, and glass industries. These and other contributions led to his receiving the 1980 ACS Award in Polymer Chemistry, along with numerous other awards during his distinguished academic career. 
