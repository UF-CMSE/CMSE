                    <p><img src="../../images/sperling1.png" alt="Dr. Lesley Sperling" align="left" hspace="10">
                    
                   One of the founding members of the <a href="http://www.lehigh.edu/engineering/academics/graduate/polymerscieng.html">Polymer Science & Engineering program at Lehigh University</a>, Lesley Sperling, died at his home on February 1, 2016. Dr. Sperling was a professor emeritus at Lehigh University after teaching at Lehigh for over 45 years. Dr. Sperling was internationally known for his work with polymer materials, and mentored many students and post-docs. He was honored as a Fellow of the <a href="http://www.acs.org/content/acs/en.html">American Chemical Society</a> in 2010. <br><br>
Dr. Sperling received a B.S. Chemistry from the <a href="http://www.ufl.edu/">University of Florida</a> in 1954, a M.S. in Chemistry from <a href="https://duke.edu/">Duke University</a> in 1957, and a Ph.D. in Chemistry from Duke University in 1959.  His PhD thesis topic was “Solution properties of cellulose esters.” Initially, he worked at Buckeye Cellulose Corp. from  Oct. 1958-June, 1965.  From July 1965-July 1967, Les was a Post Doc at Princeton University with Prof. Tobolsky. He joined Lehigh University in 1967 and retired in 2002. He held the rank of Professor in both the Chemical Engineering Department and the Materials Science and Engineering Department.  During the larger part of his time at Lehigh, Les was highly active in research that focused on interpenetrating polymer networks, IPNs. <br><br>

<p><img src="../../images/sperling2.jpg" alt="Dr. Lesley Sperling" align="right" hspace="10">

His efforts at ACS have included the POLY/PMSE Polymer Education Committee and the ACS Nomenclature Committee, as well as serving PMSE as Member at Large.  With the Poly Ed. Committee, his main activity has been interacting with chemistry book authors, trying to get them to include more polymer topics.  Les was selected as a PMSE Fellow in 2002 and as an ACS Fellow in 2010. In 2012, Les won the PMSE Distinguished Service Award.
