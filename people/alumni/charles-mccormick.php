                <td width="100%"><h1>Featured Alumni Dr. Charles McCormick</h1>
                  <p align="left">&nbsp;
                  <table width="330" height="320" border="0" align="right" cellpadding="5" cellspacing="0" bordercolor="#111111" id="AutoNumber11" style="BORDER-COLLAPSE: collapse">
                    <tr>
