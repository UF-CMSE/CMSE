                    Erik Berda, currently an assistant professor at the University of New Hampshire, received his PhD in organic chemistry from UF in 2008. Erik's career in chemistry began at <a href="http://www.chem.psu.edu/" target="_blank">Penn State University</a> where he worked as an undergraduate researcher in the <a href="http://research.chem.psu.edu/hragroup/" target="_blank">Allcock group</a> and developed a strong interest in synthetic polymer chemistry.</div>
                  <p>In 2004 he began his graduate work at UF in the <a href="http://www.chem.ufl.edu/~wagener/" target="_blank">Wagener research group</a>. &quot;I remember my decision to study at Florida vividly,&quot; Erik recalls. &quot;The recruiting weekend was in early March. When I boarded the flight in Philadelphia it was barely warm enough to keep the rain from freezing. When I deplaned in Gainesville it was 75 degrees, sunny, and I was surrounded by palm trees. I quickly noticed that a good number of the prospective students in attendance were from northern schools;<br>
                    having the recruiting weekend when we're just about fed up with winter was a brilliant tactic to say the least! In all honesty, however, it was more the collegiality of the department and the atmosphere of the Butler Laboratory than the weather that cemented the decision.&quot;</p>
                  <p>After graduating from Florida, Erik expatriated to the Netherlands for a postdoc<br>
                    with <a href="http://www.meijerlab.nl/" target="_blank">Bert Meijer</a>. In the Meijer group Erik's research focused on developing a<br>
                    process to fold synthetic polymer chains using supramolecular chemistry. &quot;The idea<br>
                    of controlling the tertiary structure of a synthetic polymer chain is currently an<br>
                    enormous synthetic challenge on the minds of many researchers in our field; I am<br>
                    very fortunate to have been involved in this work,&quot; he explains.</p>
                  <p>The cultural experience of living in a foreign country was as big a draw as<br>
                    working in a world-renowned research group. Erik developed his taste for foreign<br>
                    travel while visiting Kyoto University during his time at UF. &quot;Despite the vast<br>
                    differences in the educational systems and way of life between Japan, the US, and<br>
                    Western Europe,&quot; he states, &quot;the life of a graduate student is pretty much constant<br>
                    throughout. Even though we may have a very different set of experiences outside<br>
                    the lab, inside the lab science remains science no matter where you go. That<br>
                    common ground is a great starting point for understanding other people, making<br>
                    connections, and broadening our worldview. I hope that I can expose my own<br>
                    graduate students to similar experiences.&quot;</p>
                  <p>Erik began his independent research program at the <a href="http://www.unh.edu/chemistry/" target="_blank">University of New Hampshire</a><br>
                    in the fall of 2010 as an assistant professor of chemistry and materials science. &quot;Our<br>
                    group's focus combines many of the tricks and tactics I learned from Ken and<br>
                    Bert: we aim to use well-defined polymer structures to make nano-sized building<br>
                    blocks from discrete chains and assemble them into hierarchical structures using<br>
                    supramolecular chemistry. The ultimate goal is to establish a structure/property<br>
                    relationship between the building blocks and their hierarchical assemblies and<br>
                    really see how far we can push polymer synthesis and self-assembly as tools for<br>
                    bottom up nanofabrication.&quot; For a bit more information about Erik's research as<br>
                    well as the Department of Chemistry and Materials Science Program at UNH click
                   <a href="http://www.unh.edu/chemistry/erik-berda">here</a>.</p>
                  <p>Erik lives in Barrington, NH with his wife Dana, their dog Louie and their turtle<br>
                    Myrtle (a Gainesville native). They enjoy the outdoors, cooking, music, and traveling.<br>
                    Although research, education and family occupy a majority of his time, Erik can still<br>
                    play a pretty mean guitar.</p>
