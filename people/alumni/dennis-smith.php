                    Dr. Dennis W. Smith, Jr. received his B.S. (1988) from <a href="http://www.missouristate.edu/" target=_blank>Missouri State University</a> and his Ph.D. (1992) from the University of Florida under the guidance of <a href="http://www.chem.ufl.edu/~wagener/" target=_blank>Prof. Ken Wagener</a> on the scope and mechanism of acyclic diene metathesis (ADMET) polymerization. He was a Rhone Poulenc Graduate Research Fellow in Lyon, France pursuing novel silicone elastomers, and a Dow Chemical Postdoctoral Fellow (1993) with Dr. Raymond König in Rheinmünster, Germany exploring fundamental aspects of epoxy networks. <br>
                    <br>
                    Dr. Smith joined The Dow Chemical Company Central Research Laboratory as Sr. Research Chemist (1993), and later was promoted to Project Leader (1996) working primarily on the synthesis and characterization of high performance thermosets for thin film microelectronics applications.  Dr. Smith was an original member of the team that invented and implemented SiLK™ Semiconductor Dielectric resins, which led to commercial production (see: <a href="http://pubs.acs.org/cen/coverstory/7847/7847bus1.html" target=_blank>Chem. & Eng. News, November 20, 2000, p. 17</a>).  He served as National Chemistry Week (NCW) Coordinator (1996) and Chair of the Brazosport Section of the American Chemical Society (1997), which received a National Phoenix Award for their NCW program organized by Dr. Smith.  He received three Dow Chemical Special Recognition Awards and was a recipient of the 1997 Dow Chemical Central Research Inventor of the Year Award before joining <a href="http://chemistry.clemson.edu/" target=_blank>Clemson</a> in 1998 at the rank of Assistant Professor.  Dr. Smith was promoted to Associate Professor in 2001, granted early tenure in 2002, and promoted to Professor of Chemistry in 2006.  In 2008, Dr. Smith accepted joint appointment as Professor of Material Science & Engineering. <br>
                    <br>
                    Dr. Smith's research has led to many awards and recognition in addition to the formation of a start-up company, <a href="http://www.tetramertechnologies.com/" target=_blank>Tetramer Technologies, L.L.C.</a> founded by Dr. Smith and three other Clemson professors in 2001.  He is a recipient of a National Science Foundation Early Faculty CAREER Award, 3M Pre-Tenured Faculty Award, Clemson University Award for Faculty Achievement in the Sciences, Cottrell Scholar of Research Corporation, Clemson University Board of Trustees Award for Faculty Excellence, Universität Heidelberg Visiting Professor, Missouri State University 2007 Outstanding Alumni Award, Intel Polymer Workshop Keynote Speaker 2007, and he was named 2005 Outstanding Faculty Member by the Graduate Student Association, Clemson University Department of Chemistry. <br>
                    <br>
                    Dr. Smith has served the <a href="http://portal.acs.org/" target=_blank>American Chemical Society</a> as a member of the National Young Chemist Committee and most notably within the Division of Polymer Chemistry as National Meeting Symposia and Workshops Chair, Publicity Chair (1998-2000), Assistant Secretary (2000-2002), Workshops Committee Chair, IUPAC Liaison (2006 - ), and as Founder and Organizing Chair of the international conference, FLUOROPOLYMER 2000 - 2008, and POLYCOM:  Commercial Innovations and Opportunities in Polymer Science 2008. <br>
                    <br>
                    Dennis won national election to the post of ACS Councilor representing the Division of Polymer Chemistry on ACS Council (2002-2005) and in 2006 he was elected Vice Chair of the Division of Polymer Chemistry.  He is currently Chair-Elect and will assume the Chair position in 2009.  Dr. Smith is Editor of the journal, Polymer Bulletin, and serves on the editorial board for High Performance Polymers and Polymers for Advanced Technology.  Dr. Smith has also been named as Editor for the Handbook of Fluoropolymer Science and Technology, in progress with publisher, Wiley & Sons.  On campus, Prof. Smith was elected to the Faculty Senate and served as 2006-2007 Faculty Senate Research Committee Chair. <br>
                    <br>
                    Dr. Smith's research interest includes synthesis, mechanism, structure/property relationships, and applications of polymeric materials and composites including (1) fluoropolymers from aromatic trifluorovinyl ethers, (2) polyarylenes from bis-ortho-diynyl arenes, and (3) renewable polymers based on lactide and rubber recycling. <br>
