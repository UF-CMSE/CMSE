                  <p><em>About Megan:</em><br><br>

Even from a young age, I’ve had very high ambitions. In kindergarten I was planning on traveling the world as a pilot. By elementary school I was determined to become the first female president of the United States, and in middle school I was headed toward medical school to become a world-renowned heart surgeon. It wasn’t until high school that I found myself fascinated with chemistry and the molecular undertone of the world. I decided to continue studying <a href="http://www.chemistry.calpoly.edu/" target="_blank">chemistry at California Polytechnic State University</a> (Cal Poly) in San Luis Obispo, CA. <br><br>
During my time as an undergraduate, I was able to experience many different research opportunities. At Cal Poly I conducted research with Prof. Philip Costanzo, a synthetic polymer chemist in the department, and quickly became interested in polymer chemistry. My newfound interest in polymers then led me to the group of Prof. Jöns Hilborn at Uppsala University during my year abroad in Uppsala, Sweden. Upon my return I was able to spend a summer conducting research with <a href="http://www.chem.ufl.edu/~sumerlin/" target="_blank">Prof. Brent Sumerlin</a> at Southern Methodist University (SMU) in Dallas, TX on functional and responsive polymers before his move to the University of Florida (UF). During my time in various research groups around the U.S. and Sweden, I realized the global nature of science. It became apparent that while the idea of social and economic globalization seems rather recent, scientists have been collaborating internationally for years. Ever since, I have been especially proud to be a part of a field with such profound aspirations and an established global community. <br><br>
My experiences at Cal Poly, Uppsala, and SMU further reinforced my long-standing desire to study polymer chemistry in graduate school. Ultimately, I accepted an offer to the University of Florida. I was initially attracted to UF because of its established polymer program, the <a href="http://butlerlabs.chem.ufl.edu/" target="_blank">Butler Polymer Research Laboratory (BPRL)</a>, and a long-standing tenure of fundamental chemistry research, with the Department of Chemistry (est. 1946) being one of the oldest academic labs in the U.S. I sought to conduct focused research in polymer science, and through the help of the BPRL and the Center for Macromolecular Science and Engineering (CMSE), with its vast array of world-class macromolecular scientists, instruments, and collaborations, I believed that the facilities established at UF would allow me to achieve my goals. I knew this was an ideal opportunity to continue to expand my knowledge in the area of precision polymer synthesis, and to allow me to mature into a capable, independent scientist. <br><br>
<em>Current Work:</em>
<br><br>
I’ve grown tremendously over the last two years I’ve been in graduate school at UF both in and outside of the lab. My past research experiences in my undergraduate career focused on the synthesis of functional polymers using controlled radical polymerization techniques which has provided a good foundation for the work I’ve started as a graduate student in the Sumerlin group at UF. I have been able to further expand my synthetic skill set by focusing on the development of stimuli-responsive and biodegradable polymers while collaborating with various departments like the <a href="http://ifas.ufl.edu/" target="_blank">Institute of Food and Agricultural Sciences (IFAS)</a> to develop a site-specific nano-delivery system for agricultural production systems. Additionally, I was able to help establish and serve as president of the first POLY/PMSE student chapter at UF, an affiliation of the POLY/PMSE division of the ACS to encourage more graduate students to get involved and reinforce UF’s prominence in the polymer community. Since our introduction, POLY/PMSE has hosted several speakers from academic, industrial, and government backgrounds as well as hosted a number of social events. The continued support and encouragement I received as an undergraduate from Prof. Costanzo and Prof. Sumerlin and as a graduate student from all of the BPRL and CMSE has not get only aided me in my goals as a synthetic polymer chemist, but also in my receiving the NSF Graduate Research Fellowship in 2012. I am incredibly grateful for all the opportunities and people that have helped me pursue my passion and I look forward to doing the same for young scientists in the future. 

