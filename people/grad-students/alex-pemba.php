                  <p>When I was a boy growing up in Bulawayo, Zimbabwe, my father  taught me that if I worked hard enough, I could get anything that I dreamed.  Unfortunately, because of my origins I could not realize my primary dream of  becoming the president of the United States of America. However, there are  other dreams that have come true for me. Because of the hard work I put in as a  younger man, in 2004 I was allowed an opportunity to continue my studies in the  USA. <br>
                    <br>
                    My first stop was a small <a href="http://www.blinn.edu/" target="_blank">Blinn Community College</a> in Bryan,  Texas, <br>
                    where I was a Mathematics major until I was introduced to the world of  Chemistry. I was never that interested in Chemistry until I was inspired by my  General Chemistry teacher. I transferred to <a href="http://www.tamu.edu/" target="_blank">Texas A&amp;M University</a>, where I  continued to study Chemistry, and graduated Magna Cum Laude. After graduation  in 2008, the decision to leave Aggieland for Gatorland was an easy one. <br>
                    <br>
                    Besides the superior weather, I was attracted to UF by the  strong polymer program. The <a href="../../index.php">Center for Macromolecular Science and Engineering  (CMSE) at UF</a> is a veritable melting pot for a great number of world-class  macromolecular scientists.&nbsp; The CMSE also  has strong ties to other great institutions in the same field. The CMSE had  everything for which I was looking: characterization instruments, good sources  of funding, and great collaborations. <br>
                    <br>
                    I finally settled into the <a href="http://www.chem.ufl.edu/~miller/" target="_blank">Miller Group</a> where I research the  synthesis of new bio-derived polymers for the replacement of commodity plastics.  Besides chemistry, my time at the CMSE has allowed me to learn characterization  techniques that include: gel permeation chromatography (GPC), differential  scanning calorimetry (DSC), and thermogravimetric analysis (TGA).<br>
                    <br>
                    I consider myself blessed to have found a field for which I  have so much passion. I look forward to discovering new things about my field  everyday at work. It is this passion that I try to pass on to aspiring Gators. My  hope is to be able to continue studying biodegradable and biorenewable polymer  systems in an industrial setting when I graduate. </p>
