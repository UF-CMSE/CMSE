                  </h1>
                  <p>I grew up in a very small town in the middle of nowhere, known as Puckett, Mississippi.  Although my dad hadn't graduated from high school, he was a scientist at heart, with interests in paleontology, archeology, ichthyology, zoology, and biology. He would often take me to the woods or the river, showing me all of the aspects of nature along the way. Because of my dad, I knew that I wanted to be a scientist from an early age.
<br><br>
In junior high and early high school, I had an aptitude for science and math, and starting my junior year, I attended the <a href="http://www.themsms.org" target="_blank">Mississippi School for Mathematics and Science</a> (MSMS), a public magnet school with a heavy focus on a broad range of subjects, including statistics, calculus, physics, and chemistry. While there, I was able to take a number of courses including advanced chemistry and physics courses, along with outstanding social science classes and college level calculus and statistics courses. Of my classes at MSMS, I found my chemistry courses to be most fascinating, which ultimately led to the <a href="http://www.usm.edu/" target="_blank">University of Southern Mississippi</a> (USM), where I majored in polymer science. <br><br>
I knew nothing about polymer chemistry when joining USM. I had only heard that USM was one of the leading polymer institutes in the country, and since I loved organic chemistry, the basis of a majority of polymer chemistry, I gave it a try. I found that although organic chemistry was a vital aspect of polymer chemistry, the science of polymers was much more interdisciplinary, including aspects of physical chemistry, analytic chemistry, physics, biochemistry, materials engineering, and a broad range of mathematics. I was particularly interested in the well-defined synthesis of advanced polymer architectures and their use in a number of potential applications. 
<br><br>
<img src="../../images/william_brooks3.jpg" width="300" height="204" border="1" align="left"> <br>One technique, known as reversible addition-fragmentation chain transfer (RAFT) polymerization, piqued my interest, and no matter who I talked to from institutions across the country, everyone insisted <a href="http://www.chem.ufl.edu/~sumerlin/" target="_blank">Dr. Brent Sumerlin</a>'s work with RAFT was some of the most advanced and cutting edge. Initially I joined his group at Southern Methodist University before our group moved to join the <a href="../../index.php">Center for Macromolecular Science and Engineering</a> at the <a href="http://www.ufl.edu/" target="_blank">University of Florida</a>. <br><br><br>
Our group has shown that microwave heating can lead to enhanced reaction kinetics with reaction control similar to or better than conventional heating conditions. My current research focuses on the synthesis of advanced materials, employing microwave irradiation as a heating source and was recently recognized at the 8<sup>th</sup> Annual International Symposium on Stimuli-Responsive Materials, winning a poster presentation award, and will be highlighted in the Royal Society of Chemistry journal <em>Polymer Chemistry</em>.</p>
