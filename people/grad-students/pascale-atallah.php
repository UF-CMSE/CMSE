                  </h1>
                  <p>In 1990, when I was 7 years old, Lebanon was struggling with one of the biggest civil wars it had ever experienced. In order to stay on top of their curriculum, my sisters and their friends used to gather at our house every morning and my mum would teach them chemistry, amongst other subjects. I used to sit by the door and watch silently. After lunch, I would sneak in, and pretend I was a chemistry teacher, instead of playing with dolls. Throughout high school, it became clear that I wanted to become a chemist. <br><br> 

I received my Bachelor's degree at <a href="http://www.usj.edu.lb/en/" target="_blank">Saint Joseph University in Lebanon</a>, a French affiliated school. Then I decided to pursue higher education in France and received a Master's degree in Physics and Chemical Engineering at <a href="http://www.enscbp.fr/" target="_blank">ENSCPB (Ecole Nationale Superieure de Chimie Physique de Bordeaux)</a>. At the end of my three years, I had to apply the various theoretical skills I had learned in a 4 months internship. I did my internship then worked for a year in Paris, for a company called <a href="http://www.rhodia.us/" target="_blank">Rhodia</a>. My goal was to develop a library of diblock polymers using a robot called CHEMSPEED. During my time there, I was able to observe the different roles within a company and research groups, and after long conversations with my superiors, it became obvious to me that I wanted more. I then decided to apply to the PhD program at the <a href="http://www.chem.ufl.edu/" target="_blank">department of Chemistry at UF</a>. At the time, I only knew UF through their basketball achievements, but my supervisors had nothing but great things to say about all the professors at UF. <br><br> 

I arrived in the United States in 2008 and joined <a href="http://www.chem.ufl.edu/~wagener/" target="_blank">Dr. Ken Wagener's research group</a>. My project deals with the development of polyolefins processing siloxy groups with precision that could be used as extruding aids, which requires both chemistry and chemical engineering knowledge. I enjoy this project because of its industrial implication and the possible applications that could result from it. Once I have synthesized the polymers and characterized them at the <a href="../../people">CMSE</a>, I am hoping to spend a couple of months in Germany with our collaborator and supporter Lyondell-Basell in order to learn about rheology and how to make rheological measurements. <br>
<br> 

Being part of the <a href="http://butlerlabs.chem.ufl.edu/" target="_blank">Butler Polymer Research lab</a> and the <a href="../../index.php">CMSE</a> has brought not only science knowledge to me but has also taught me great sense of community, sharing and communication. It has allowed me to learn and understand fields other than the ones directly related to my research through discussions with my lab mates from a variety of research groups. I also learned a lot about various cultures due to the multicultural diversity of the students.<br><br>  

As each day passes, I see my dream of becoming a PhD in Chemistry becoming closer. However, I can't forget all the people that have contributed in every piece of it: my mom, for initiating me to chemistry; the team for which I worked at Rhodia (Gerard Bacquet, James Wilson and Matthias Destarac) for pushing me to pursue graduate studies and specifically Dr. Aponick and Dr. Wagener who have helped me transition from a chemical engineer to a synthetic organic chemist. Last but not least I am very grateful for Lyondell-Basell for giving me the opportunity to work on this project and support me in any way available. </p>
